public class ForLoopExample {
    public static void main(String[] args) {
        System.out.println("Incepe numaratoarea normala:");

        for (int i = 1; i <= 5; i++) {
            System.out.println("Numarul este: " + i);
        }

        System.out.println("Numaratoarea normala s-a incheiat.");

        System.out.println("Incepe numaratoarea inversa:");

        for (int i = 5; i >= 1; i--) {
            System.out.println("Numarul este: " + i);
        }

        System.out.println("Numaratoarea inversa s-a incheiat.");
    }

}
